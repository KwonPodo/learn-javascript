// var
/* 1. Declaration
    can overwrite declarations without errors
    선언과 재할당 모두 얼마든지 가능하다.
*/
var camper = "jay";
var camper = "joon";

// let
/* 1. Declaration
    when using let, variable with the same name can be only be declared once.
    선언은 한번만, 재할당은 얼마든지 가능하다.
*/
let camper = "jay";
let camper = "joon"; // this will cause an error.
